function getCurrentLanguage() {
  return localStorage.getItem('xocozy-language') || 'fr';
}

function applyTranslations(lang = getCurrentLanguage()) {
  const dictionary = translations[lang] || translations.fr;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (dictionary[key]) {
      element.setAttribute('placeholder', dictionary[key]);
    }
  });
  document.querySelectorAll('[data-i18n-label]').forEach((element) => {
    const key = element.getAttribute('data-i18n-label');
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });
}

function setLanguage(lang) {
  localStorage.setItem('xocozy-language', lang);
  applyTranslations(lang);
  const switcher = document.getElementById('languageSwitcher');
  if (switcher) {
    switcher.value = lang;
  }
}

window.addEventListener('DOMContentLoaded', () => {
  const switcher = document.getElementById('languageSwitcher');
  if (switcher) {
    switcher.value = getCurrentLanguage();
    switcher.addEventListener('change', (event) => setLanguage(event.target.value));
  }
  applyTranslations();
});
